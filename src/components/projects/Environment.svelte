<h4>About</h4>
<p>
	Planned from the start to become a settlement building game (I have a soft spot for these kind of
	games), I needed a dynamic environment that could be randomized every game. There are plenty of
	finished terrain generators in the Unity asset store, but since I wanted to support more than
	terrain and biomes (and to challenge myself) I developed my own.
</p>
<p>
	In its current state it supports randomized islands, biomes, nature parameters (fertility,
	altitude preferences etc), randomized placement for natural props (tree types, rocks, plants,
	grass etc).
</p>
<h4>Challenges</h4>
<p>
	Developing an atlas generator was tougher than anticipated, but being naive in any type of
	development is mostly a good thing in my experience! I think the hardest was to debug when
	something went wrong, it could either be my code or my input that is causing a bug.
</p>
<h4>What I learned</h4>
<p>
	I learned a lot from this project. LUT (Look up tables), weight based algorithms, modelling in
	blender.
</p>
